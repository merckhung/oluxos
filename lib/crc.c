u32 CRC32( s8 *Buf, u32 Len ) {

    u32 i, Res;


    Res = 0xFFFFFFFFUL;
    for( i = 0 ; i < Len ; i++, Buf++ ) {
    
        Res = CRC32Table[ (Res ^ *Buf) & 0xFFUL ] ^ (Res >> 8);
    }

    
    return (Res ^ 0xFFFFFFFFUL);
}



u16 CRC16( s8 *Buf, u32 Len ) {

    u32 i;
    u16 Res;


    Res = 0xFFFF;
    for( i = 0 ; i < Len ; i++, Buf++ ) {
    
        Res = CRC16Table[ (Res ^ *Buf) & 0xFUL ] ^ (Res >> 4);
    }

    
    return (Res ^ 0xFFFF);


}







